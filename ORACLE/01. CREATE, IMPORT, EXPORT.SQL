-- [CREATE DATABASE]
-- 1. Login to SYSTEM / SYS AS SYS DBA
CONN SYS AS SYS DBA;


-- 2. TABLESPACE
CREATE TABLESPACE TABLESPACE_NAME DATAFILE 'FILE DBF LOCATION' SIZE SIZE_IN_MB;
--EXAMPLE :
CREATE TABLESPACE DATAUSER DATAFILE 'C:\oraclexe\oradata\XE\DATAUSER01.DBF' SIZE 100MB;

-- For resize tablespace
ALTER DATABASE DATAFILE 'FILE DBF LOCATION' RESIZE SIZE_IN_MB;
-- Example ;
ALTER DATABASE DATAFILE 'C:\oraclexe\oradata\XE\DATAUSER01.DBF' RESIZE 200M;



-- 3. Create User and Set Default Tablespace
CREATE USER USER_NAME IDENTIFIED BY USER_PASSWORD;


